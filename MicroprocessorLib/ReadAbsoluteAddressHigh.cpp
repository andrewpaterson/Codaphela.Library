#include "W65C816.h"
#include "W65C816State.h"
#include "ReadAbsoluteAddressHigh.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressHigh::Init(NotMemoryLock bNotMemoryLock, bool bNotVectorPull)
{
	CDataOperation::Init(false, true, bNotMemoryLock, true, bNotVectorPull);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressHigh::Execute(CW65C816* pcCPU)
{
	//Is this the right Data?
	pcCPU->GetState()->SetAddressHigh((uint8)pcCPU->GetState()->GetData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressHigh::Print(CChars* psz)
{
	psz->Append("Read(AAH)");
}

