#include "DataOperation.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CDataOperation::Init(bool bValidProgramAddress, bool bValidDataAddress, NotMemoryLock bNotMemoryLock, bool bRead, bool bNotVectorPull)
{
    mbValidProgramAddress = bValidProgramAddress;
    mbValidDataAddress = bValidDataAddress;
    mbNotMemoryLock = bNotMemoryLock;
    mbRead = bRead;
    mbNotVectorPull = bNotVectorPull;
    mbReady = true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsData(void)
{
    return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsRead(void)
{
    return mbRead;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsValidProgramAddress(void)
{
    return mbValidProgramAddress;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsValidDataAddress(void)
{
    return mbValidDataAddress;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsNotMemoryLock(void)
{
    return mbNotMemoryLock;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsNotVectorPull(void)
{
    return mbNotVectorPull;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CDataOperation::IsReady(void)
{
    return mbReady;
}

