//
//////////////////////////////////////////////////////////////////////////
void CW65C816Pins::Init(CMetaBus16* pcAddress, CMetaBus8* pcData, CMetaTrace* pcPHI2, CMetaTrace* pcRESB, CMetaTrace* pcRWB)
{
	mpcAddress = pcAddress;
	mpcData = pcData;
	mpcVPB = NULL;
	mpcRDY = NULL;
	mpcABORTB = NULL;
	mpcIRQB = NULL;
	mpcNMIB = NULL;
	mpcMLB = NULL;
	mpcVPA = NULL;
	mpcVDA = NULL;
	mpcRESB = pcRESB;
	mpcMX = NULL;
	mpcPHI2 = pcPHI2;
	mpcBE = NULL;
	mpcE = NULL;
	mpcRWB = pcRWB;
}


