#include "W65C816Pins.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CW65C816Pins::Init(CMetaBus16* pcAddress, CMetaBus8* pcData)
{
	mpcAddress = pcAddress;
	mpcData = pcData;
	mbVPB = false;
	mbRDY = false;
	mbABORTB = false;
	mbIRQB = false;
	mbNMIB = false;
	mbMLB = false;
	mbVPA = false;
	mbVDA = false;
	mbRESB = false;
	mbMX = false;
	mbPHI2 = false;
	mbBE = false;
	mbE = false;
	mbRWB = false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CW65C816Pins::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CW65C816Pins::Reset(void)
{
	mbRDY = true;
	mbABORTB = true;
	mbIRQB = true;
	mbNMIB = true;
	mbRESB = false;
	mbPHI2 = false;
	mbBE = true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CW65C816Pins::HalfCycle(void)
{
	mbPHI2 = !mbPHI2;
}


