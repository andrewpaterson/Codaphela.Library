void CNMIVector::Init(void)
{
    CInterruptVector::Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CNMIVector::GetAddress(CW65C816* pcCPU)
{
    if (pcCPU->IsEmulation())
    {
        return 0xfffa;
    }
    else
    {
        return 0xffea;
    }
}

