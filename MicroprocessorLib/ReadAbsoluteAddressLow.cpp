#include "W65C816.h"
#include "W65C816State.h"
#include "ReadAbsoluteAddressLow.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressLow::Init(NotMemoryLock bNotMemoryLock, bool bNotVectorPull)
{
	CDataOperation::Init(false, true, bNotMemoryLock, true, bNotVectorPull);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressLow::Execute(CW65C816* pcCPU)
{
	//Is this the right Data?
	pcCPU->GetState()->SetAddressLow((uint8)pcCPU->GetState()->GetData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressLow::Print(CChars* psz)
{
	psz->Append("Read(AAL)");
}

