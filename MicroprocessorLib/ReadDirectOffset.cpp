#include "W65C816.h"
#include "W65C816State.h"
#include "ReadDirectOffset.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDirectOffset::Init(NotMemoryLock bNotMemoryLock)
{
	CDataOperation::Init(true, false, bNotMemoryLock, true, true);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDirectOffset::Execute(CW65C816* pcCPU)
{
	//This is not the 16bit Direct Page this is 8bit direct offset D0.
	pcCPU->GetState()->SetDirectOffset(pcCPU->GetState()->GetData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDirectOffset::Print(CChars* psz)
{
	psz->Append("Read(D0)");
}

