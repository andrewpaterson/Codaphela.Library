//////////////////////////////////////////////////////////////////////////
void CNoteFourY::Init(bool bNextWillRead)
{
	COperation::Init();

	mbNextWillRead = bNextWillRead;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CNoteFourY::Execute(CW65C816* pcCPU)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CNoteFourY::IsInitialSide(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CNoteFourY::MustExecute(CW65C816* pcCPU)
{
	return pcCPU->GetState()->NoteFourY(mbNextWillRead);
}


