#include "W65C816.h"
#include "W65C816State.h"
#include "ReadDataLow.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataLow::Init(NotMemoryLock bNotMemoryLock)
{
	CDataOperation::Init(false, true, bNotMemoryLock, true, true);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataLow::Execute(CW65C816* pcCPU)
{
	pcCPU->GetState()->SetDataLow(pcCPU->GetState()->GetPData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CReadDataLow::IsInitialSide(void)
{
	return false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataLow::Print(CChars* psz)
{
	psz->Append("Read(DL)");
}

