#include "W65C816.h"
#include "ReadAbsoluteAddressBank.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressBank::Init(NotMemoryLock bNotMemoryLock)
{
	CDataOperation::Init(false, true, bNotMemoryLock, true, true);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressBank::Execute(CW65C816* pcCPU)
{
	pcCPU->SetAddressBank(pcCPU->GetIOData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CReadAbsoluteAddressBank::IsInitialSide(void)
{
	return false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadAbsoluteAddressBank::Print(CChars* psz)
{
	psz->Append("Read(AAB)");
}

