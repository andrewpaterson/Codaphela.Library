//
//////////////////////////////////////////////////////////////////////////
int32 CAbsoluteAddressHigh::GetOffset(CW65C816* pcCPU)
{
    return pcCPU->GetState().GetAddress().GetOffset() & 0xff00;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CAbsoluteAddressHigh::Print(CChars* psz)
{
    psz->Append("AAH");
}


