void CCOPVector::Init(void)
{
    CInterruptVector::Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CCOPVector::GetAddress(CW65C816* pcCPU)
{
    if (pcCPU->IsEmulation())
    {
        return 0xfff4;
    }
    else
    {
        return 0xffe4;
    }
}

