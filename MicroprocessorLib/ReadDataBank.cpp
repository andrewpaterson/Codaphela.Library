#include "W65C816.h"
#include "W65C816State.h"
#include "ReadDataBank.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataBank::Init(NotMemoryLock bNotMemoryLock)
{
	CDataOperation::Init(false, true, bNotMemoryLock, true, true);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataBank::Execute(CW65C816* pcCPU)
{
    pcCPU->GetState()->SetDataBank(pcCPU->GetState()->GetIOData());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CReadDataBank::IsInitialSide(void)
{
	return false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CReadDataBank::Print(CChars* psz)
{
	psz->Append("Read(DBR)");
}

