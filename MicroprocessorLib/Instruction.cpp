#include "Instruction.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CInstruction::Init(uint16 uiCode, CInstructionCycles* pcCycles, char* szName, char* szDescription)
{
    mszDescription.Init(szDescription);
    muiCode = uiCode;
    mszName.Init(szName);
    meAddressingMode = pcCycles->GetAddressingMode();
    mpcCycles = pcCycles;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CInstruction::Kill(void)
{
    mpcCycles->Kill();
    mpcCycles = NULL;

    mszName.Kill();
    mszDescription.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CInstruction::GetCode(void)
{
    return muiCode;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CInstruction::GetName(void)
{
    return mszName.Text();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CInstructionCycles* CInstruction::GetCycles(void)
{
    return mpcCycles;
}


