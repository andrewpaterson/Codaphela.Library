#include "W65C816.h"
#include "W65C816State.h"
#include "WriteAbsoluteAddressHigh.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CWriteAbsoluteAddressHigh::Init(void)
{
	CDataOperation::Init(false, true, true, false, true);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CWriteAbsoluteAddressHigh::Execute(CW65C816* pcCPU)
{
	pcCPU->GetState()->SetPData(GetHighByte(pcCPU->GetState()->GetAddress()->GetOffset()));
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CWriteAbsoluteAddressHigh::IsInitialSide(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CWriteAbsoluteAddressHigh::Print(CChars* psz)
{
	psz->Append("Write(AAH)");
}

