void CW65C816::GetOpcodeValueHex(CChars* psz, int cycle, CInstruction* pcInstruction)
{
    uint16  uiCode;

    if (cycle != 0)
    {
        uiCode = pcInstruction->GetCode();
        if (uiCode <= 255)
        {
            psz->Append("0x");
            To8BitHexString(psz, (uint8)uiCode);
        }
        else
        {
            psz->Append("---");
        }
    }
    else
    {
        psz->Append("###");
    }
}

