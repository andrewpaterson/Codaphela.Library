void ToPointerString(void* pv, char* szString, size iStringLength)
{
	CChars	c;

	IntToString(szString, iStringLength, (uint32)pv, 16);

	c.Init(szString);
	c.RightAlign('0', 8);
	c.Insert(0, "0x");
	strcpy(szString, c.Text());
	c.Kill();
}


