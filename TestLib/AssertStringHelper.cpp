bool IsMultiLine(const char* szExpected, const char* szActual)
{
	int		iCount;

	iCount = CountNewLines((char*)szExpected, StrLen(szExpected));
	if (iCount > 0)
	{
		return true;
	}

	iCount = CountNewLines((char*)szActual, StrLen(szActual));
	if (iCount > 0)
	{
		return true;
	}

	return false;
}


