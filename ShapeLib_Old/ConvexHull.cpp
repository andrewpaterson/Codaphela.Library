#include "ConvexHull.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CConvexHullImm::Init(void)
{
	mcPlanes.Init(8);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CConvexHullImm::Kill(void)
{
	mcPlanes.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CPlaneImm* CConvexHullImm::AddPlane(void)
{
	return mcPlanes.Add();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CConvexHullImm::AddPlane(CPlaneImm* pcPlane)
{
	mcPlanes.Add(pcPlane);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CConvexHullImm::Save(CChunkFile* pcFile)
{
	pcFile->WriteArrayTemplate(&mcPlanes);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CConvexHullImm::Load(CChunkFile* pcFile)
{
	pcFile->ReadArrayTemplate(&mcPlanes);
}

