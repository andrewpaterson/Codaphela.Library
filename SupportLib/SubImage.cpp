//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
SInt32Vec2 CSubImage::GetImageSize(void)
{
	SInt32Vec2	sSize;

	sSize.Init(GetImageWidth(), GetImageHeight());
	return sSize;
}


