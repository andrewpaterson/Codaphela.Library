void CTextUTF16Multi::Init(size uiByteSize)
{
	CTextDrawable::Init(uiByteSize);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
uint8* CTextUTF16Multi::GetChar(void)
{
	return (uint8*)RemapSinglePointer(this, sizeof(CTextUTF16Multi));
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
size CTextUTF16Multi::CharByteLength(void)
{
	return muiNumChars;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CTextUTF16Multi::Copy(void* pviData)
{
	memcpy_fast(GetChar(), pviData, muiNumChars);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
size CTextUTF16Multi::ByteSize(void)
{
	return muiNumChars;
}


