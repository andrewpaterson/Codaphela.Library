#include "BaseImageRowBlitter.h"

//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CBaseImageRowBlitter::Init(Ptr<CImage> pSource, Ptr<CImage> pDest)
{
	mpSource = pSource;
	mpDest = pDest;

	miSourcePixelStride = (size)pSource->GetPixelByteStride();
	miDestPixelStride = (size)pDest->GetPixelByteStride();
	miSourceWidth = pSource->GetWidth();
	miDestWidth = pDest->GetWidth();
	miSourceRowStride = miSourceWidth * miSourcePixelStride;
	miDestRowStride = miDestWidth * miDestPixelStride;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CBaseImageRowBlitter::Kill(void)
{
	mpSource = NULL;
	mpDest = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CImage> CBaseImageRowBlitter::GetSource(void)
{
	return mpSource;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CImage> CBaseImageRowBlitter::GetDest(void)
{
	return mpDest;
}

