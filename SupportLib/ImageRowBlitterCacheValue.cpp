bool CImageRowBlitterCacheDest::Is(Ptr<CImage> pDestImage, const char* szBlitterClass)
{
	if (mpDestImage.IsEqual(pDestImage))
	{
		if (StringCompare(mpcBlitter->ClassName(), szBlitterClass) == 0)
		{
			return true;
		}
	}
	return false;
}


