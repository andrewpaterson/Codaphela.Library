//////////////////////////////////////////////////////////////////////////
void CTriangleHelper::Init(void)
{
	mpsPosition = &masPosition[0];
	mpsPosition1 = &masPosition[1];
	mpsPosition2 = &masPosition[2];
	mpsNormal = &msNormal;
}


