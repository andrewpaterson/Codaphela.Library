//////////////////////////////////////////////////////////////////////////
void CGlyph::Init(Ptr<CImageCel> pCel, int16 iStep)
{
	mpCel = pCel;
	miStep = iStep;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGlyph::Class(void)
{
	M_Pointer(mpCel);
	U_Int16(miStep);
}


