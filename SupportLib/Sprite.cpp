#include "Sprite.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CSprite::Init(CSpriteType* pcType, int32 x, int32 y)
{
	msPosition.Init(x, y);
	mpcType = pcType;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CSprite::Kill(void)
{
	mpcType = NULL;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
CSpriteType* CSprite::GetType(void) { return mpcType; }
SInt2* CSprite::GetPosition(void) { return &msPosition; }

