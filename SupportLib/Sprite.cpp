#include "Sprite.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CSprite::Init(Ptr<CImageCel> pCel, int32 x, int32 y)
{
	PreInit();

	msPosition.Init(x, y);
	mpCel = pCel;

	PostInit();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CSprite::Free(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CSprite::Class(void)
{
	U_2Int32(msPosition);
	M_Pointer(mpCel);
	M_Pointer(mpBlitter);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool CSprite::Load(CObjectReader* pcFile)
{
	return false;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool CSprite::Save(CObjectWriter* pcFile)
{
	return false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CSprite::GetImageDestBounds(CRectangle* pcReturn)
{
	mpCel->GetImageDestBounds(msPosition.x, msPosition.y, pcReturn);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CSprite::CreateBlitter(Ptr<CImageCelBlitterCache> pBlitterCache)
{
	mpBlitter = pBlitterCache->CreateImageBlitter(mpCel);
	return mpBlitter.IsNotNull();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CSprite::ClearBlitter(void)
{
	mpBlitter = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CSprite::Blit(int32 iDestX, int32 iDestY)
{
	mpBlitter->Blit(iDestX, iDestY);
	return true;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
Ptr<CImageCel> CSprite::GetCel(void) { return mpCel; }
int32 CSprite::GetX(void) {	return msPosition.x; }
int32 CSprite::GetY(void) {	return msPosition.y; }

