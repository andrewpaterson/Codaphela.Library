#include "JavaSyntaxPackage.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxPackage::Init(CJavaSyntaxTree* pcTree, CJavaSyntax* pcParent)
{
	CJavaSyntax::Init(pcTree, pcParent);
	mapcIdentifiers.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxPackage::Kill(void)
{
	mapcIdentifiers.Kill();
	CJavaSyntax::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaSyntaxPackage::GetType(void)
{
	return "Package";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxPackage::TypePrint(CChars* pszDest, int iDepth)
{
	CJavaSyntax::TypePrint(pszDest, iDepth);
	PrettyPrint(pszDest);
	pszDest->AppendNewLine();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxPackage::PrettyPrint(CChars* pszDest, int iBlockDepth)
{
	int						i;
	CJavaTokenIdentifier* pcIdentifier;

	for (i = 0; i < mapcIdentifiers.NumElements(); i++)
	{
		if (i != 0)
		{
			pszDest->Append('.');
		}

		pcIdentifier = mapcIdentifiers.GetPtr(i);
		pcIdentifier->Print(pszDest);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxPackage::AddIdentifier(CJavaTokenIdentifier* pcIdentifier)
{
	mapcIdentifiers.Add(pcIdentifier);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CJavaSyntaxPackage::IsPackage(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CIdentifierArray* CJavaSyntaxPackage::GetIdentifiers(void)
{
	return &mapcIdentifiers;
}

