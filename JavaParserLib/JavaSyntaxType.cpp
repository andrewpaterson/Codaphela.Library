#include "JavaSyntaxGeneric.h"
#include "JavaSyntaxType.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxType::Init(CJavaSyntaxTree* pcTree, CJavaSyntax* pcParent)
{
	CJavaSyntaxTypeCommon::Init(pcTree, pcParent);

	mpcName = NULL;
	mpcGeneric = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxType::Kill(void)
{
	mpcName = NULL;
	mpcGeneric = NULL;

	CJavaSyntaxTypeCommon::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaSyntaxType::GetType(void)
{
	return "Type";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxType::TypePrint(CChars* pszDest, int iDepth)
{
	CJavaSyntaxTypeCommon::TypePrint(pszDest, iDepth);
	pszDest->AppendNewLine();

	if (mpcGeneric)
	{
		mpcGeneric->TypePrint(pszDest, iDepth + 1);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxType::PrettyPrint(CChars* pszDest, int iBlockDepth)
{
	CJavaSyntaxTypeCommon::PrettyPrint(pszDest);
	if (mpcGeneric)
	{
		mpcGeneric->PrettyPrint(pszDest);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CJavaSyntaxType::IsType(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxType::SetGeneric(CJavaSyntaxGeneric* pcGeneric) { mpcGeneric = pcGeneric; }
CJavaSyntaxGeneric* CJavaSyntaxType::GetGeneric(void) { return mpcGeneric; }

