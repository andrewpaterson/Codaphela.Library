#include "JavaSyntaxClassCommon.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxClassCommon::Init(CJavaSyntaxTree* pcTree, CJavaSyntax* pcParent)
{
	CJavaSyntaxStatement::Init(pcTree, pcParent);
	mcModifiers.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxClassCommon::Kill(void)
{
	mcModifiers.Kill();
	CJavaSyntaxStatement::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaSyntaxClassCommon::GetType(void)
{
	return "Class (Common)";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxClassCommon::TypePrint(CChars* pszDest, int iDepth)
{
	CJavaSyntaxStatement::TypePrint(pszDest, iDepth);
	mcModifiers.Print(pszDest);
	pszDest->AppendNewLine();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxClassCommon::PrettyPrint(CChars* pszDest, int iBlockDepth)
{
	PrintModifiers(pszDest, mcModifiers);
	pszDest->Append(GetType());
	pszDest->Append(' ');
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CJavaSyntaxClassCommon::IsClassCommon(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CJavaModifiers* CJavaSyntaxClassCommon::GetModifiers(void)
{
	return &mcModifiers;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxClassCommon::SetModifiers(CJavaModifiers cModifiers)
{
	mcModifiers = cModifiers;
}

