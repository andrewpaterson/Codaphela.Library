#include "JavaKeyword.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaKeyword::Init(CJavaKeywordDefinition* pcKeyword)
{
	CJavaToken::Init();
	mpcKeyword = pcKeyword;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaKeyword::Kill(void)
{
	mpcKeyword = NULL;
	CJavaToken::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaKeyword::Print(CChars* pszDest)
{
	pszDest->Append(mpcKeyword->GetName());
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaKeyword::GetType(void)
{
	return "Keyword";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaKeywordDefinition::Init(EJavaKeyword eKeyword, char* szName)
{
	meKeyword = eKeyword;
	mszName.Init(szName);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaKeywordDefinition::Kill(void)
{
	meKeyword = JK_Unknown;
	mszName.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaKeywordDefinition::GetName(void)
{
	return mszName.Text();
}

