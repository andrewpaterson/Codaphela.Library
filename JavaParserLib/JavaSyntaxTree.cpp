#include "JavaSyntaxTree.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxTree::Init(void)
{
	mpcRoot = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxTree::Kill(void)
{
	mpcRoot = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxTree::SetRoot(CJavaSyntaxFile* pcRoot)
{
	mpcRoot = pcRoot;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxTree::TypePrint(CChars* pszDest)
{
	mpcRoot->TypePrint(pszDest, 0);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaSyntaxTree::PrettyPrint(CChars* pszDest)
{
	mpcRoot->PrettyPrint(pszDest, 0);
	return pszDest->Text();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CJavaSyntaxFile* CJavaSyntaxTree::GetRoot(void)
{
	return mpcRoot;
}

