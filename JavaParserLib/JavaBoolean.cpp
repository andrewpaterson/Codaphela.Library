#include "JavaBoolean.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaBoolean::Init(BOOL bValue)
{
	mbValue = bValue;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaBoolean::Kill(void)
{
	mbValue = FALSE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaBoolean::Print(CChars* pszDest)
{
	if (mbValue)
	{
		pszDest->Append("true");
	}
	else
	{
		pszDest->Append("false");
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CJavaBoolean::IsBoolean(void) { return TRUE; }

