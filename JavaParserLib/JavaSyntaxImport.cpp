#include "JavaSyntaxImport.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::Init(CJavaSyntaxTree* pcTree, CJavaSyntax* pcParent)
{
	CJavaSyntax::Init(pcTree, pcParent);
	mapcIdentifiers.Init();
	mbStatic = FALSE;
	mbWild = FALSE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::Kill(void)
{
	mbStatic = FALSE;
	mbWild = FALSE;
	mapcIdentifiers.Kill();
	CJavaSyntax::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CJavaSyntaxImport::GetType(void)
{
	return "Import";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::Print(CChars* pszDest, int iDepth)
{
	int						i;
	CJavaTokenIdentifier* pcIdentifier;

	CJavaSyntax::Print(pszDest, iDepth);

	for (i = 0; i < mapcIdentifiers.NumElements(); i++)
	{
		if (i != 0)
		{
			pszDest->Append('.');
		}

		pcIdentifier = mapcIdentifiers.GetPtr(i);
		pcIdentifier->Print(pszDest);
	}

	pszDest->AppendNewLine();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::AddIdentifier(CJavaTokenIdentifier* pcIdentifier)
{
	mapcIdentifiers.Add(pcIdentifier);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::SetStatic(BOOL bStatic)
{
	mbStatic = bStatic;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CJavaSyntaxImport::SetWild(BOOL bWild)
{
	mbWild = bWild;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CJavaSyntaxImport::IsImport(void)
{
	return TRUE;
}

