//////////////////////////////////////////////////////////////////////////
void CTextField::Init(CViewport* pcViewport, char* szString, BOOL bDefaultBorder, BOOL bDefaultBackground)
{
	CLabel::Init(pcViewport, szString, bDefaultBorder, bDefaultBackground);

	mcEditor.Init(&mpcText->mcTextData, szString);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTextField::Kill(void)
{
	mcEditor.Kill();
	CLabel::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CTextField::Draw(void)
{
	return CLabel::Draw();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTextField::Layout(SInt2 sPosition, SInt2 sAreaSize)
{
	mpcText->msCaretPos = mcEditor.mcEditor.GetEditPosition();
	CLabel::Layout(sPosition, sAreaSize);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTextField::FocusGained(void)
{
	StartTimer(600, TRUE);
	mpcText->mbCaretVisible = TRUE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTextField::FocusLost(void)
{
	StopTimer();
	mpcText->mbCaretVisible = FALSE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTextField::TimerAlarm(float fOversleep)
{
	mpcText->mbCaretVisible = !mpcText->mbCaretVisible;
}


