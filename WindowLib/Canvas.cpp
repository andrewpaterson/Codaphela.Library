{
	mpcNativeCanvas->DrawPixel(iX, iY, sColour);
}

//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CCanvas::IsValid(void)
{
	if ((meFormat == CF_Unknown) ||
		(miWidth == -1) ||
		(miHeight == -1))
	{
		return false;
	}
	return true;
}


