void CNativeCanvas::Init(CCanvas* pcCanvas, CNativeWindowFactory* pcWindowFactory)
{
	CBasicNativeComponent::Init(pcWindowFactory);
	mpcCanvas = pcCanvas;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CNativeCanvas::Kill(void)
{
	mpcCanvas = NULL;
	CBasicNativeComponent::Kill();
}


