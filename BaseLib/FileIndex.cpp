#include "FileIndex.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIndex::Init(void)
{
	miFile = -1;
	mulliFilePos = -1LL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIndex::Init(int iFile, filePos ulliFilePos)
{
	miFile = iFile;
	mulliFilePos = ulliFilePos;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIndex::Kill(void)
{
	miFile = -1;
	mulliFilePos = 0;
}

//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CFileIndex::HasFile(void)
{
	return miFile != -1;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFileIndex::SetIndex(int iFile, filePos ulliFilePos)
{
	miFile = iFile;
	mulliFilePos = ulliFilePos;

}

