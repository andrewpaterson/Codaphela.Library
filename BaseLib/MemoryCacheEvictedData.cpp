#include "MemoryCacheEvictedData.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CMemoryCacheEvictedData::Init(void)
{
	mcEvictedData.Init();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CMemoryCacheEvictedData::Kill(void)
{
	mcEvictedData.Kill();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
BOOL CMemoryCacheEvictedData::CacheDataEvicted(void* pvData, size_t iDataSize)
{
	pvData = mcEvictedData.Add(pvData, iDataSize);
	return pvData != NULL;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
int CMemoryCacheEvictedData::NumElements(void)
{
	return mcEvictedData.NumElements();
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CMemoryCacheEvictedData::Get(int iIndex, size_t* puiDataSize)
{
	return mcEvictedData.Get(iIndex, puiDataSize);
}

