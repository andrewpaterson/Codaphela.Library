//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool CFileReader::ReadString(char* szString, size iMaxLength)
{
	size		iLength;

	if (!ReadStringLength(&iLength))
	{
		return false;
	}

	if (iLength > iMaxLength - 1)
	{
		iLength = iMaxLength - 1;
	}

	if (!ReadStringChars(szString, iLength))
	{
		return false;
	}
	szString[iLength] = 0;
	return true;
}


