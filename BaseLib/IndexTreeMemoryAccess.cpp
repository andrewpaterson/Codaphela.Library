{
	return mpcTree->NumElements();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::ValidateIndex(void)
{
	return mpcTree->ValidateIndexTree();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Put(void* pvKey, int iKeySize, void* pvData, size_t iDataSize)
{
	void* pvResult;

	pvResult = mpcTree->Put(pvKey, iKeySize, pvData, iDataSize);
	return pvResult != NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Get(void* pvKey, int iKeySize, void* pvData, size_t* piDataSize, size_t uiMaxDataSize)
{
	void*	pvResult;
	size_t	iDataSize;

	pvResult = mpcTree->Get(pvKey, iKeySize, &iDataSize);
	
	SafeAssign(piDataSize, iDataSize);

	if (pvResult)
	{
		if (pvData)
		{
			memcpy(pvData, pvResult, MinDataSize(iDataSize, uiMaxDataSize));
		}
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Remove(void* pvKey, int iKeySize)
{
	return mpcTree->Remove(pvKey, iKeySize);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Has(void* pvKey, int iKeySize)
{
	return mpcTree->HasKey(pvKey, iKeySize);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int CIndexTreeMemoryAccess::DataSize(void* pvKey, int iKeySize)
{
	size_t	iDataSize;
	void*	pvData;

	pvData = mpcTree->Get(pvKey, iKeySize, &iDataSize);
	if (pvData)
	{
		return iDataSize;
	}
	else
	{
		return 0;
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Flush(void* pvKey, int iKeySize)
{
	if (Has(pvKey, iKeySize))
	{
		return TRUE;
	}
	return FALSE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
BOOL CIndexTreeMemoryAccess::Evict(void* pvKey, int iKeySize)
{
	return FALSE;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CIndexTreeIterator* CIndexTreeMemoryAccess::CreateIterator(void)
{
	CIndexTreeMemoryIterator*	pcIter;

	pcIter = NewMalloc<CIndexTreeMemoryIterator>();
	pcIter->Init(mpcTree);

	return pcIter;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexTreeMemoryAccess::FreeIterator(CIndexTreeIterator* pcIter)
{
	SafeKill(pcIter);
}

