#include "IndexTreeIterator.h"



//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexTreeIterator::Init(int iType)
{
	miType = iType;
	mbStarted = FALSE;
	Clear();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexTreeIterator::Kill(void)
{
	miType = 0;
	mbStarted = FALSE;
	Clear();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
size_t CIndexTreeIterator::GetDataSize(void)
{
	return miDataSize;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void* CIndexTreeIterator::GetData(void)
{
	return macData;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
size_t CIndexTreeIterator::GetKeySize(void)
{
	return miKeySize;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CIndexTreeIterator::GetKey(void)
{
	return macKey;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexTreeIterator::Clear(void)
{
	memset_fast(macKey, 0, MAX_KEY_SIZE);
	memset_fast(macData, 0, MAX_DATA_SIZE);
	miDataSize = 0;
	miKeySize = 0;
}

