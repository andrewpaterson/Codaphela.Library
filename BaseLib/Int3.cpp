#include "DataIO.h"
#include "Int3.h"


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3::SInt3()
{
	x = 0;
	y = 0;
	z = 0;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3::SInt3(int fx, int fy, int fz)
{
	x = fx;
	y = fy;
	z = fz;
}


/////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void SInt3::Init(int x, int y, int z)
{
	this->x = x;
	this->y = y;
	this->z = z;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void SInt3::Zero(void)
{
	x = 0;
	y = 0;
	z = 0;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool SInt3::operator == (const SInt3& v) const
{
	return x == v.x && y == v.y && z == v.z;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3 SInt3::operator + (const SInt3& v) const
{
	return SInt3(x + v.x, y + v.y, z + v.z);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3 SInt3::operator - (const SInt3& v) const
{
	return SInt3(x - v.x, y - v.y, z - v.z);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3& SInt3::operator += (const SInt3& v)
{
	x += v.x;
	y += v.y;
	z += v.z;
	return *this;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
SInt3& SInt3::operator -= (const SInt3& v)
{
	x -= v.x;
	y -= v.y;
	z -= v.z;
	return *this;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool SInt3::Save(CFileWriter* pcFileWriter)
{
	ReturnOnFalse(pcFileWriter->WriteInt32(x));
	ReturnOnFalse(pcFileWriter->WriteInt32(y));
	ReturnOnFalse(pcFileWriter->WriteInt32(z));
	return true;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool SInt3::Load(CFileReader* pcFileReader)
{
	ReturnOnFalse(pcFileReader->ReadInt32(&x));
	ReturnOnFalse(pcFileReader->ReadInt32(&y));
	ReturnOnFalse(pcFileReader->ReadInt32(&z));
	return true;
}

