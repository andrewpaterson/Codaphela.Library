//////////////////////////////////////////////////////////////////////////
void CSystemAllocator::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CSystemAllocator::Malloc(size uiSize)
{
	return malloc(uiSize);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
bool CSystemAllocator::Free(void* pv)
{
	if (pv)
	{
		free(pv);
		return true;
	}
	else
	{
		return false;
	}
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CSystemAllocator::Realloc(void* pv, size uiSize)
{
	if (pv == NULL)
	{
		pv = malloc(uiSize);
	}
	else
	{
		pv = realloc(pv, uiSize);
	}
	return pv;
}


