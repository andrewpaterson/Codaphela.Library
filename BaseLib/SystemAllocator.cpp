void* CSystemAllocator::Malloc(size_t tSize)
{
	return malloc(tSize);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CSystemAllocator::Free(void* pv)
{
	free(pv);
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CSystemAllocator::Realloc(void* pv, size_t tSize)
{
	if (pv == NULL)
	{
		pv = malloc(tSize);
	}
	else
	{ 
		pv = realloc(pv, tSize);
	}
	return pv;
}


