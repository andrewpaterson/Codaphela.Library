//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CChars CIndexAccess::GetStringString(char* pszKey, size uiMaxDataSize)
{
	size	iKeySize;
	bool	bResult;
	CChars	sz;
	uint32	uiDataSize;

	if (StrEmpty(pszKey))
	{
		sz.Init();
		return sz;
	}

	iKeySize = strlen(pszKey);

	uiDataSize = DataSize((uint8*)pszKey, iKeySize);
	if (uiDataSize != 0)
	{
		sz.InitLength(uiDataSize);
		bResult = Get((uint8*)pszKey, iKeySize, sz.Text(), NULL, uiMaxDataSize);
		return sz;
	}
	else
	{
		sz.Init();
		return sz;
	}
}


