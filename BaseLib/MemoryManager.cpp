void CMemoryManager::Init(void* pvHeapStart, void* pvHeapEnd)
{
	mpvHeapStart = pvHeapStart;
	mpvHeapEnd = pvHeapEnd;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMemoryManager::Kill(void)
{
	mpvHeapStart = NULL;
	mpvHeapEnd = NULL;
}

