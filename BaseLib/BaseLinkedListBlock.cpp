//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CBaseLinkedListBlock::Kill(void)
{
	SLLNode*	psNode;
	SLLNode*	psNode2;

	psNode = mcList.GetHead();
	while (psNode)
	{
		psNode2 = mcList.GetNext(psNode);
		Free(psNode);
		psNode = psNode2;
	}
	mcList.Kill();
}


