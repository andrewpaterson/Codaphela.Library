//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CBaseLinkedListBlock::GetNext(void* pvData)
{
	SLLNode*	psNode;

	psNode = DataGetNode(pvData);
	psNode = mcList.GetNext(psNode);
	if (psNode)
	{
		return NodeGetData(psNode);
	}
	return NULL;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void* CBaseLinkedListBlock::GetPrev(void* pvData)
{
	SLLNode*	psNode;

	psNode = DataGetNode(pvData);
	psNode = mcList.GetPrev(psNode);
	if (psNode)
	{
		return NodeGetData(psNode);
	}
	return NULL;
}


