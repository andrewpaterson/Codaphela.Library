{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int CRandom::Next(int iMin, int iMax)
{
	std::uniform_int_distribution<int> range(iMin, iMax);
	
	return range(mcRNG);
}

