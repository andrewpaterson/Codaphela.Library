#include <chrono>
#include "DataTypes.h"
#include "StdRandom.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CRandom::Init(void)
{
	std::random_device r;
	std::default_random_engine eng{ r() };
	std::uniform_int_distribution<int> urd(0, MAX_INT);
	int iSeed = urd(eng);
	Init(iSeed);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CRandom::Init(int iSeed)
{
	mcRNG.seed(iSeed);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CRandom::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int CRandom::Next(int iMin, int iMaxInclusive)
{
	std::uniform_int_distribution<int> range(iMin, iMaxInclusive);
	
	return range(mcRNG);
}

