void CMallocator::FreeMultiple(CArrayVoidPtr* pav)
{
	int		iNumElements;
	int		i;
	void*	pv;

	pav->QuickSort();
	iNumElements = pav->NumElements();
	for (i = iNumElements - 1; i >=0; i--)
	{
		pv = pav->GetPtr(i);
		Free(pv);
	}
}

