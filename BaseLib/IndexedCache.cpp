//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void* CIndexedCache::TestGetDescriptor(OIndex oi)
{
	SIndexedCacheDescriptor*	psDesc;
	void*						pvData;

	pvData = StartIteration();
	while (pvData)
	{
		psDesc = GetDescriptor(pvData);
		if (psDesc->oi == oi)
		{
			return pvData;
		}
		pvData = Iterate(pvData);
	}
	return NULL;
}


