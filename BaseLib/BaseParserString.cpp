#include "BaseParserString.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void SBaseParserString::Init(size uiColumn, size uiLine)
{
	this->uiColumn = uiColumn;
	this->uiLine = uiLine;
	this->szString.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void SBaseParserString::Kill(void)
{
	szString.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* SBaseParserString::GetString(void)
{
	return szString.Text();
}

