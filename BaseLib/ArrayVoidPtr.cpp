//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
void CArrayVoidPtr::Add(void* pv)
{
	void**	pvTemp;

	pvTemp = __CArrayVoidPtr::Add();
	*pvTemp = pv;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
BOOL CArrayVoidPtr::Get(int iIndex, void** pv)
{
	void**	pvTemp;

	pvTemp = __CArrayVoidPtr::SafeGet(iIndex);
	if (pvTemp)
	{
		*pv = *pvTemp;
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}


