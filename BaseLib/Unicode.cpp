size GetUnicodeCodePointLength(uint16 uiChar)
{
	if (uiChar < 0xff)
	{
		return 1;
	}
	return 2;
}


//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
size GetUnicodeCodePointLength(uint32 uiChar)
{
	if (uiChar < 0xff)
	{
		return 1;
	}
	if (uiChar < 0xffff)
	{
		return 2;
	}
	if (uiChar < 0xffffff)
	{
		return 3;
	}
	return 4;
}

