#include "DataField.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CDataField::Init(CClass* pcFieldClass, ptrdiff_t iOffset, CClass* pcContainingClass)
{
	if (iOffset >= 0)
	{
		CField::Init(pcFieldClass, iOffset, pcContainingClass);
	}
	else
	{
		CField::Init(NULL, 0, pcContainingClass);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CDataField::Kill(void)
{
	CField::Kill();
}

