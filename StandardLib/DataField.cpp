#include "DataField.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CDataField::Init(EPrimitiveType eType, ptrdiff_t iOffset)
{
	if (iOffset >= 0)
	{
		CField::Init(eType, iOffset);
	}
	else
	{
		CField::Init(PT_Undefined, 0);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CDataField::Kill(void)
{
	CField::Kill();
}

