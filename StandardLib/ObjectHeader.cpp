//////////////////////////////////////////////////////////////////////////
void CPointerHeader::Init(void)
{
	mcType = 0;
	mszObjectName.Zero();
	moi = 0;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerHeader::Kill(void)
{
	mszObjectName.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectHeader::Init(void)
{
	//This should not call CPointerHeader::Init.
	mszClassName.Zero();
	miClassSize = 0;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectHeader::Kill(void)
{
	mszClassName.Kill();
	CPointerHeader::Kill();
}

