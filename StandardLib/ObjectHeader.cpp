#include "ObjectFileGeneral.h"
#include "ObjectHeader.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectIdentifier::Init(void)
{
	meType = OBJECT_POINTER_NULL;
	moi = INVALID_O_INDEX;
	mszObjectName.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectIdentifier::Init(OIndex oi, uint16 uiSize)
{
	meType = OBJECT_POINTER_ID;
	moi = oi;
	mszObjectName.Init();
	muiSize = uiSize;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectIdentifier::Init(const char* szName, OIndex oi, uint16 uiSize)
{
	meType = OBJECT_POINTER_NAMED;
	moi = oi;
	mszObjectName.Fake(szName);
	muiSize = uiSize;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectIdentifier::Kill(void)
{
	mszObjectName.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CObjectIdentifier::IsNamed(void)
{
	if (meType != OBJECT_POINTER_NAMED)
	{
		return false;
	}
	
	if (mszObjectName.Empty())
	{
		return false;
	}

	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CObjectIdentifier::IsIndexed(void)
{
	if (meType == OBJECT_POINTER_ID)
	{
		return true;
	}

	if (meType == OBJECT_POINTER_NAMED && mszObjectName.Empty())
	{
		return true;
	}

	return false;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CObjectIdentifier::IsFat(void)
{
	return muiSize != OBJECT_IDENTIFIER_SIZE_NOT_SET;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CObjectIdentifier::GetName(void)
{
	if (meType == OBJECT_POINTER_NAMED)
	{
		if (!mszObjectName.Empty())
		{
			return mszObjectName.Text();
		}
	}
	return "";
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
OIndex CObjectIdentifier::GetIndex()
{
	if (meType == OBJECT_POINTER_ID || meType == OBJECT_POINTER_NAMED)
	{
		return moi;
	}

	return INVALID_O_INDEX;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CObjectIdentifier::GetType(void)
{
	if (meType == OBJECT_POINTER_ID)
	{
		return "IDX";
	}
	else if (meType == OBJECT_POINTER_NAMED)
	{
		return "NAM";
	}
	else if (meType == OBJECT_POINTER_NULL)
	{
		return "NUL";
	}
	else
	{
		return NULL;
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CObjectIdentifier::GetFatSize(void)
{
	return muiSize;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectHeader::Init(void)
{
	mszClassName._Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CObjectHeader::Kill(void)
{
	mszClassName.Kill();
	CObjectIdentifier::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CObjectHeader::ClassName(void)
{
	return mszClassName.Text();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CObjectHeader::GetIdentifier(CChars* psz)
{
	char* szName;

	psz->Append(ClassName());
	psz->Append(":");
	psz->Append(GetIndex());
	szName = GetName();
	if (!StrEmpty(szName))
	{
		psz->Append(":\"");
		psz->Append(szName);
		psz->Append("\"");
	}
	return psz->Text();
}


