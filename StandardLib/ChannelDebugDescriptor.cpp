//////////////////////////////////////////////////////////////////////////
void CChannelDebugDescriptor::Init(char* szLongName, char* szShortName)
{
	mszLongName = szLongName;
	mszShortName = szShortName;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CChannelDebugDescriptor::Kill(void)
{
	mszLongName = NULL;
	mszShortName = NULL;
}

