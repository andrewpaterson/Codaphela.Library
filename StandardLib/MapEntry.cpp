#include "ClassDefines.h"
#include "ObjectWriter.h"
#include "ObjectReader.h"
#include "MapEntry.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CMapEntry::CMapEntry()
{
}

//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CMapEntry::~CMapEntry()
{
	Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::operator = (CMapEntry& cOther)
{
	Kill();
	Init();
	mpKey = cOther.KeyObject();
	mpValue = cOther.ValueObject();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CMapEntry::CMapEntry(CMapEntry& cOther)
{
	Init();
	mpKey = cOther.KeyObject();
	mpValue = cOther.ValueObject();
}

//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Class(void)
{
	M_Pointer(mpKey);
	M_Pointer(mpValue);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(void)
{
	PreInit();
	mpKey = NULL;;
	mpValue = NULL;;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(CPointer& pKey, CPointer& pValue)
{
	PreInit();
	PostInit();
	mpKey = pKey;
	mpValue = pValue;
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(CBaseObject* pcKey, CBaseObject* pcValue)
{
	PreInit();
	PostInit();
	mpKey = pcKey;
	mpValue = pcValue;
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Free(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Clear(void)
{
	mpKey = NULL;;
	mpValue = NULL;;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CMapEntry::Exists(void)
{
	return mpKey.IsNotNull();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CPointer CMapEntry::Key(void)
{
	return mpKey;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CPointer CMapEntry::Value(void)
{
	return mpValue;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CBaseObject* CMapEntry::KeyObject(void)
{
	return (CBaseObject*)mpKey.Object();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CBaseObject* CMapEntry::ValueObject(void)
{
	return (CBaseObject*)mpValue.Object();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CMapEntry::ValidatePointersEmbedded(void)
{
	bool bValid;

	bValid = mpKey.Embedding() == this;
	bValid &= mpValue.Embedding() == this;
	return bValid;
}

