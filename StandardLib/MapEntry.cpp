#include "ClassDefines.h"
#include "ObjectWriter.h"
#include "ObjectReader.h"
#include "MapEntry.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Class(void)
{
	M_Pointer(mpKey);
	M_Pointer(mpValue);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(void)
{
	PreInit();
	mpKey = NULL;;
	mpValue = NULL;;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(CPointer& pKey, CPointer& pValue)
{
	PreInit();
	mpKey = pKey;
	mpValue = pValue;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CMapEntry> CMapEntry::Init(CBaseObject* pcKey, CBaseObject* pcValue)
{
	PreInit();
	mpKey = pcKey;
	mpValue = pcValue;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Free(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CMapEntry::Clear(void)
{
	mpKey = NULL;;
	mpValue = NULL;;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CMapEntry::Exists(void)
{
	return mpKey.IsNotNull();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CPointer CMapEntry::Key(void)
{
	return mpKey;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CPointer CMapEntry::Value(void)
{
	return mpValue;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CBaseObject* CMapEntry::KeyObject(void)
{
	return (CBaseObject*)mpKey.Object();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CBaseObject* CMapEntry::ValueObject(void)
{
	return (CBaseObject*)mpValue.Object();
}

