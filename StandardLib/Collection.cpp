//////////////////////////////////////////////////////////////////////////
bool CCollection::RemoveAll(void)
{
	bool	bResult;

	bResult = RemoveAllPointerTosTryFree();

#ifdef _DEBUG
	ValidateObjectsConsistency(true);
#endif // _DEBUG

	return bResult;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CCollection::AddObjectFrom(CEmbeddedObject* pcObject, bool bResult, bool bValidate)
{
	if (bResult && pcObject)
	{
		if (IsEmbeddingContainerAllocatedInObjects())
		{
			pcObject->AddHeapFrom(this, bValidate);
		}
		else
		{
			pcObject->AddStackFrom(this, bValidate);
		}
	}
	return bResult;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CCollection::RemoveObjectTryFree(CEmbeddedObject* pcObject, bool bResult, bool bValidate)
{
	if (bResult && pcObject)
	{
		if (IsEmbeddingContainerAllocatedInObjects())
		{
			bResult = pcObject->RemoveHeapFromTryFree(this, bValidate);
		}
		else
		{
			bResult = pcObject->RemoveStackFromTryFree(this, bValidate);
		}
	}
	return bResult;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CCollection::RemovePointerToTryFree(CBaseObject* pcPointedTo)
{
	if (pcPointedTo)
	{
		if (IsEmbeddingContainerAllocatedInObjects())
		{
			return pcPointedTo->RemoveHeapFromTryFree(this, false);
		}
		else
		{
			return pcPointedTo->RemoveStackFromTryFree(this, true);
		}
	}
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CCollection::RemovePointerToDontFree(CBaseObject* pcPointedTo)
{
	if (IsEmbeddingContainerAllocatedInObjects())
	{
		RemoveToFromDontFree(pcPointedTo);
		return true;
	}
	else
	{
		//I'm not sure if that can ever be called.
		return gcLogger.Error2(__METHOD__, " is not yet implemented for Stack-Froms.", NULL);
	}
}


