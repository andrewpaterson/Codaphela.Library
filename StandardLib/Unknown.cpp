//////////////////////////////////////////////////////////////////////////
CUnknown::CUnknown(void)
{
	mpcUnknownsThisIn = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CUnknown::SetUnknowns(CUnknowns* pcUnknowns)
{
	mpcUnknownsThisIn = pcUnknowns;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CUnknown::Kill(void)
{
	if (mpcUnknownsThisIn)
	{
		mpcUnknownsThisIn->RemoveInKill(this);
	}
}


