#include "PointerField.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerField::Init(ptrdiff_t iOffset)
{
	if (iOffset >= 0)
	{
		CField::Init(PT_Pointer, iOffset);
	}
	else
	{
		CField::Init(PT_Undefined, 0);
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerField::Kill(void)
{
	CField::Kill();
}

