#include "ClassDefines.h"
#include "ObjectWriter.h"
#include "ObjectReader.h"
#include "PointerContainer.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerContainer::Class(void)
{
	M_Pointer(mp);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CPointerContainer> CPointerContainer::Init(void)
{
	PreInit();
	mp = NULL;;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CPointerContainer> CPointerContainer::Init(CPointer& pPointer)
{
	PreInit();
	mp = pPointer;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
Ptr<CPointerContainer> CPointerContainer::Init(CEmbeddedObject* pcObject)
{
	PreInit();
	mp = pcObject;
	PostInit();
	return this;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerContainer::Free(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CPointerContainer::Clear(void)
{
	mp = NULL;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CEmbeddedObject* CPointerContainer::PointerObject(void)
{
	return mp.Object();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CEmbeddedObject** CPointerContainer::PointerObjectPtr(void)
{
	return mp.ObjectPtr();
}

