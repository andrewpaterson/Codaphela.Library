//////////////////////////////////////////////////////////////////////////
//																		//
//																		//
//////////////////////////////////////////////////////////////////////////
BOOL CSafeArrayBlock::MemcpyDestAndUnlock(void* pvSource, void* pvDest)
{
	if (pvSource != NULL)
	{
		memcpy(pvDest, pvSource, c.ElementSize());
		m.unlock();
		return TRUE;
	}
	else
	{
		m.unlock();
		return FALSE;
	}
}

