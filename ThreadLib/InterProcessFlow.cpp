#include "BaseLib/Chars.h"
#include "InterProcessFlow.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CInterProcessFlow::Init(char* szSharedMemoryName)
{
	mcSharedMemory.Init(szSharedMemoryName);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CInterProcessFlow::Init(char* szSharedMemoryName, char* szSharedMemoryNamePostfix)
{
	CChars	sz;

	sz.Init(szSharedMemoryName)->Append(szSharedMemoryNamePostfix);
	Init(sz.Text());
	sz.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CInterProcessFlow::Kill(void)
{
	mcSharedMemory.Close();
	mcSharedMemory.Kill();
}

