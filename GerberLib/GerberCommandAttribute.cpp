#include "GerberCommandAttribute.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandAttribute::Init(EGerberCommandEnum eType, size iNameLength)
{
	CGerberCommand::Init(eType);
	mszName.InitLength(iNameLength);
	mszValues.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandAttribute::Kill(void)
{
	mszValues.Kill();
	mszName.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CGerberCommandAttribute::NameText(void)
{
	return mszName.Text();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CGerberCommandAttribute::AddValue(size iValueLength)
{
	CChars* psz;

	psz = mszValues.AddLength(iValueLength);
	return psz->Text();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
size CGerberCommandAttribute::NumValues(void)
{
	return mszValues.NumElements();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CGerberCommandAttribute::ValueText(size uiIndex)
{
	return mszValues.GetText(uiIndex);
}

