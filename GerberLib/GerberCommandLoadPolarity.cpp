#include "GerberCommandLoadPolarity.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandLoadPolarity::Init(EGerberPolarity ePolarity)
{
	CGerberCommand::Init(GC_LP);
	mePolarity = ePolarity;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandLoadPolarity::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandLoadPolarity::IsLoadPolarity(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandLoadPolarity::IsDark(void)
{
	return mePolarity == GP_Dark;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandLoadPolarity::IsClear(void)
{
	return mePolarity == GP_Clear;
}

