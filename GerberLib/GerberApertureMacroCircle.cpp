#include "GerberApertureMacroCircle.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberApertureMacroCircle::Init(void)
{
	CGerberApertureMacro::Init(GAMP_Circle);
	mcExposure.Init();
	mcDiameter.Init();
	mcCenterX.Init();
	mcCenterY.Init();
	mcRotation.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberApertureMacroCircle::Kill(void)
{
	mcRotation.Kill();
	mcCenterY.Kill();
	mcCenterX.Kill();
	mcDiameter.Kill();
	mcExposure.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CGerberExpression* CGerberApertureMacroCircle::GetExposure(void)
{
	return &mcExposure;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CGerberExpression* CGerberApertureMacroCircle::GetDiameter(void)
{
	return &mcDiameter;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CGerberExpression* CGerberApertureMacroCircle::GetCenterX(void)
{
	return &mcCenterX;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CGerberExpression* CGerberApertureMacroCircle::GetCenterY(void)
{
	return &mcCenterY;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CGerberExpression* CGerberApertureMacroCircle::GetRotation(void)
{
	return &mcRotation;
}

