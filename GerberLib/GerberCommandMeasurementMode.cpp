#include "GerberCommandMeasurementMode.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandMeasurementMode::Init(EGerberMeasurementMode eMode)
{
	CGerberCommand::Init(GC_MO);
	meMode = eMode;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandMeasurementMode::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandMeasurementMode::IsMeasurementMode(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandMeasurementMode::IsMillimeters(void)
{
	return meMode == GM_Millimeters;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandMeasurementMode::IsInches(void)
{
	return meMode == GM_Inches;
}


