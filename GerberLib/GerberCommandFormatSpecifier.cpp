#include "GerberCommandFormatSpecifier.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandFormatSpecifier::Init(uint16 uiXWholes, uint16 uiXDecimals, uint16 uiYWholes, uint16 uiYDecimals)
{
	CGerberCommand::Init(GC_FS);
	muiXWholes = uiXWholes;
	muiXDecimals = uiXDecimals;
	muiYWholes = uiYWholes;
	muiYDecimals = uiYDecimals;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CGerberCommandFormatSpecifier::Kill(void)
{
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CGerberCommandFormatSpecifier::IsFormatSpecifier(void)
{
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CGerberCommandFormatSpecifier::GetXWholeNumbers(void)
{
	return muiXWholes;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CGerberCommandFormatSpecifier::GetXDecimals(void)
{
	return muiYDecimals;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CGerberCommandFormatSpecifier::GetYWholeNumbers(void)
{
	return muiYWholes;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
uint16 CGerberCommandFormatSpecifier::GetYDecimals(void)
{
	return muiYDecimals;
}

