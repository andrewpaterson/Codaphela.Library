#include "FatInfoSectorCache.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFatInfoSectorCache::Init(uint16 uiBytesPerSector)
{
	muiBytesPerSector = uiBytesPerSector;
	mllcCachedSectors.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CFatInfoSectorCache::Kill(void)
{
	mllcCachedSectors.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
SFatSectorCache* CFatInfoSectorCache::AddCache(uint32 uiFatSharedBufferSector)
{
	SFatSectorCache*	psSectorCache;

	psSectorCache = (SFatSectorCache*)mllcCachedSectors.Add(sizeof(SFatSectorCache));
	return psSectorCache;
}

