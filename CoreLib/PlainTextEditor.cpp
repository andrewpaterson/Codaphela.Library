//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
SInt2 CPlainTextEditor::GetPosition(int iPosition)
{
	char	c;
	SInt2	sPos;

	sPos.x = 0;
	sPos.y = 0;

	for (int i = 0; i < iPosition; i++)
	{
		c = mszText.GetChar(i);
		if (c == '\0')
		{
			break;
		}
		else if (c == '\n')
		{
			sPos.y++;
			sPos.x = 0;
		}
		else
		{
			sPos.x++;
		}
	}
	return sPos;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int CPlainTextEditor::FindPositionAlongLine(int iStartOfLine, int iCharCount)
{
	int		iPos;
	char	c;
	int		iCountedSoFar;

	if (iCharCount == 0)
	{
		return iStartOfLine;
	}

	iPos = iStartOfLine;
	iCountedSoFar = 0;
	for (;;)
	{
		c = mszText.GetChar(iPos);
		if ((c == '\n') || (c == '\0'))
		{
			return iPos;
		}
		else if (iCountedSoFar == iCharCount)
		{
			return iPos;
		}
		else if (iCountedSoFar > iCharCount)
		{
			return iPos-1;
		}

		iCountedSoFar = CountChar(iCountedSoFar, c);
		iPos++;
	}
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int CPlainTextEditor::CountChar(int iCharCount, char c)
{
	int		iTabMod;

	if (c == '\t')
	{
		iTabMod = iCharCount % miTabWidth;
		if (iTabMod == 0)
		{
			iTabMod = 4;
		}
		else
		{
			iTabMod = 4 - iTabMod;
		}
		iCharCount += iTabMod;
	}
	else
	{
		iCharCount++;
	}
	return iCharCount;
}


