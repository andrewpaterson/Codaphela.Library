//
//
//////////////////////////////////////////////////////////////////////////
void CPlainTextEditor::Enter(void)
{
	char	szTemp[2];

	if (!IsSelected())
	{
		mszText.Insert(miEditPos, '\n');
		Right();
	}
	else
	{
		szTemp[0] = '\n';
		szTemp[1] = 0;
		OverwriteSelection(szTemp);
	}
}


