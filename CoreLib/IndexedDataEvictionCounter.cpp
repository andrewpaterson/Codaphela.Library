#include "IndexedDataEvictionCounter.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexedDataEvictionCounter::Init(void)
{
	miEvictions = 0;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CIndexedDataEvictionCounter::Reset(void)
{
	miEvictions = 0;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
bool CIndexedDataEvictionCounter::IndexEvicted(OIndex oi, void* pvData, int iDataSize)
{
	miEvictions++;
	return true;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
int64 CIndexedDataEvictionCounter::EvictionCount(void)
{
	return miEvictions;
}

