#include "TransientSequence.h"


CTransientSequence	gcTransientSequence;


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTransientSequence::Init(void)
{
	moiNext = FIRST_O_INDEX;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CTransientSequence::Kill(void)
{
	moiNext = INVALID_O_INDEX;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
OIndex CTransientSequence::GetNext(void)
{
	OIndex	oi;

	oi = moiNext;
	moiNext++;
	return oi;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
OIndex CTransientSequence::PeekNext(void)
{
	return moiNext;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TransientSequenceInit(void)
{
	gcTransientSequence.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void TransientSequenceKill(void)
{
	gcTransientSequence.Kill();
}

